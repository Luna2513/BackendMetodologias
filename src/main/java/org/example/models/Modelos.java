package org.example.models;

public class Modelos {
    String Profesor;
    String Alumno;
    String Asignatura;
    String Nota;
    String Fecha;

    public Modelos(String profesor, String alumno, String asignatura, String nota, String fecha) {
        this.Profesor = profesor;
        this.Alumno = alumno;
        this.Asignatura = asignatura;
        this.Nota = nota;
        this.Fecha = fecha;
    }

    

}
